export const effectSummary = effect => {
  const type = effectTypeLabel(effect.type);
  const target = targetLabel(effect.target);
  const duration = effect.duration && effect.duration !== DURATIONS.INSTANT ? ` (${durationLabel(effect.duration)})` : "";
  const condition = effect.condition ? ` | ${conditionLabel(effect.condition)}` : "";
  const amount = effect.amount ? ` ${effect.amount}` : "";
  const details = [
    effect.nature,
    effect.energy_type,
    effect.attribute,
    effect.card_name,
    effect.tag,
    Array.isArray(effect.natures) ? effect.natures.join(", ") : "",
    Array.isArray(effect.tags) ? effect.tags.join(", ") : "",
    effect.card_type,
    effect.meta_key && effect.meta_value ? `${effect.meta_key}: ${effect.meta_value}` : "",
    effect.condition_type ? ACTIVE_CONDITION_LABELS[effect.condition_type] || effect.condition_type : "",
    effect.dice_type,
    effect.value_formula,
    effect.description,
  ].filter(Boolean);
  const extra = details.length ? ` | ${details.join(" / ")}` : "";
  const targetFilters = normalizeTargetFilters(effect.target_filters);
  const filters = targetFilters.length === 1
    ? ` | Filtro: ${abilityConditionLabel(targetFilters[0].type)}`
    : targetFilters.length > 1
      ? ` | Filtros: ${targetFilters.length}`
      : "";
  return `${type}${amount} - ${target}${duration}${condition}${extra}${filters}`;
};

