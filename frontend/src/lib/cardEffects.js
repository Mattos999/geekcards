export const normalizeEffectType = effect => {
  if (!effect?.type) return null;
  if (LEGACY_CONDITION_EFFECT_TYPES.has(effect.type)) return null;

  let type = EFFECT_TYPE_ALIASES[effect.type] || effect.type;
  let target = normalizeTargetValue(effect.target);
  let conditionType = effect.condition_type || effect.conditionType || "";

  if (effect.type === EFFECT_TYPES.APPLY_CONDITION) {
    type = statusEffectFromConditionType(conditionType);
  } else if (effect.type === EFFECT_TYPES.BLOCK_ACTION) {
    type = String(conditionType || "").toUpperCase().includes("RETREAT")
      ? EFFECT_TYPES.PREVENT_RETREAT
      : EFFECT_TYPES.PREVENT_ATTACK;
  } else if (effect.type === EFFECT_TYPES.IMMUNITY) {
    type = effect.applies_to === "NEGATIVE_EFFECTS"
      ? EFFECT_TYPES.IMMUNE_TO_NEGATIVE_EFFECTS
      : EFFECT_TYPES.IMMUNE_TO_DAMAGE_TYPE;
  } else if (effect.type === EFFECT_TYPES.SWITCH_ACTIVE) {
    type = String(effect.target || "").startsWith("OPPONENT")
      ? EFFECT_TYPES.SWITCH_OPPONENT_ACTIVE
      : EFFECT_TYPES.SWITCH_OWN_ACTIVE;
  } else if (effect.type === EFFECT_TYPES.IF_DICE_GREATER_THAN) {
    type = EFFECT_TYPES.DICE_ROLL;
  } else if (effect.type === EFFECT_TYPES.IF_DICE_LESS_THAN) {
    type = EFFECT_TYPES.DICE_ROLL;
  }

  if (effect.type === EFFECT_TYPES.ADD_ENERGY_TO_ACTIVE) target = TARGETS.SELF_ACTIVE;
  if (effect.type === EFFECT_TYPES.ADD_ENERGY_TO_BENCH) target = TARGETS.SELF_BENCH;
  if (effect.type === EFFECT_TYPES.HEAL_SELF || effect.type === EFFECT_TYPES.HEAL_ACTIVE) target = TARGETS.SELF_ACTIVE;
  if ([EFFECT_TYPES.HEAL_BENCH, EFFECT_TYPES.HEAL_BENCH_CARD, EFFECT_TYPES.HEAL_BENCH_GROUP].includes(effect.type)) {
    target = TARGETS.SELF_BENCH;
  }
  if ([EFFECT_TYPES.HEAL_ANY_SELF_CARD, EFFECT_TYPES.HEAL_ANY_ALLY].includes(effect.type)) {
    target = TARGETS.ANY_SELF_CARD;
  }
  if (effect.type === EFFECT_TYPES.HEAL_EQUIPPED_CARD) target = TARGETS.EQUIPPED_CARD;

  if (!CANONICAL_EFFECT_TYPES.has(type)) return null;
  return { type, target, condition_type: conditionType };
};

const normalizeAbilityTrigger = trigger => ABILITY_TRIGGER_ALIASES[trigger] || trigger;

const normalizeConditionValue = value => {
  if (Array.isArray(value)) return value.map(item => String(item).trim()).filter(Boolean);
  if (typeof value === "string" && value.includes(",")) {
    return value.split(",").map(item => item.trim()).filter(Boolean);
  }
  return value ?? "";
};

const normalizeAbilityCondition = condition => {
  if (!condition?.type) return null;
  const type = condition.type;
  if (type === ABILITY_CONDITION_TYPES.SOURCE_IS_ACTIVE) {
    return { ...condition, type: ABILITY_CONDITION_TYPES.SOURCE_POSITION, value: ABILITY_POSITION_VALUES.ACTIVE };
  }
  if (type === ABILITY_CONDITION_TYPES.SOURCE_IS_ON_BENCH) {
    return { ...condition, type: ABILITY_CONDITION_TYPES.SOURCE_POSITION, value: ABILITY_POSITION_VALUES.BENCH };
  }
  if (type === ABILITY_CONDITION_TYPES.TARGET_IS_ACTIVE) {
    return { ...condition, type: ABILITY_CONDITION_TYPES.TARGET_POSITION, value: ABILITY_POSITION_VALUES.ACTIVE };
  }
  if (type === ABILITY_CONDITION_TYPES.TARGET_IS_ON_BENCH) {
    return { ...condition, type: ABILITY_CONDITION_TYPES.TARGET_POSITION, value: ABILITY_POSITION_VALUES.BENCH };
  }
  if (type === ABILITY_CONDITION_TYPES.SOURCE_META_IN) {
    return { ...condition, type: ABILITY_CONDITION_TYPES.SOURCE_META_VALUE_IN };
  }
  if (type === ABILITY_CONDITION_TYPES.TARGET_META_IN) {
    return { ...condition, type: ABILITY_CONDITION_TYPES.TARGET_META_VALUE_IN };
  }
  if (type === ABILITY_CONDITION_TYPES.TARGET_USES_ELEMENT) {
    return {
      ...condition,
      type: ABILITY_CONDITION_TYPES.TARGET_META_VALUE_IN,
      value: {
        key: "elements",
        values: normalizeStringList(condition.value),
      },
    };
  }
  return condition;
};

export const normalizeTargetFilters = filters => (
  Array.isArray(filters)
    ? filters
        .map(normalizeAbilityCondition)
        .filter(Boolean)
        .filter(filter => filter?.type && TARGET_FILTER_TYPES.has(filter.type))
        .map(filter => ({
          type: filter.type,
          value: normalizeConditionValue(filter.value),
          negate: Boolean(filter.negate),
        }))
    : []
);

const valuesMatchAny = (value, candidates) => {
  const expected = Array.isArray(value)
    ? value
    : String(value || "").split(",").map(item => item.trim()).filter(Boolean);
  return expected.some(item => candidates.includes(item));
};

const cardTagsUpper = card => [
  ...(card?.tags || []),
  ...(card?.meta?.tags || []),
].map(tag => String(tag).trim().toUpperCase()).filter(Boolean);

const normalizeStatusFilterValue = value => {
  const normalized = String(value || "").trim().toUpperCase();
  const map = {
    POISON: "poison",
    BURN: "burn",
    PARALYSIS: "paralyze",
    PARALYZE: "paralyze",
    FREEZE: "freeze",
    CONFUSED: "confuse",
    CONFUSE: "confuse",
    SLEEP: "sleep",
    PREVENT_ATTACK: "prevent_attack",
    PREVENT_RETREAT: "prevent_retreat",
    BLOCK_RETREAT: "block_retreat",
  };
  return map[normalized] || normalized.toLowerCase();
};

export const targetFilterMatchesCard = (card, filter) => {
  if (!card || !filter?.type) return false;
  const result = (() => {
    switch (filter.type) {
      case ABILITY_CONDITION_TYPES.TARGET_NATURE_IN:
        return valuesMatchAny(filter.value, card.natures || []);
      case ABILITY_CONDITION_TYPES.TARGET_CARD_TYPE_IN:
        return valuesMatchAny(filter.value, [card.card_type].filter(Boolean));
      case ABILITY_CONDITION_TYPES.TARGET_HAS_TAG: {
        const expected = Array.isArray(filter.value) ? filter.value : String(filter.value || "").split(",");
        const normalized = expected.map(item => String(item).trim().toUpperCase()).filter(Boolean);
        return normalized.length > 0 && normalized.some(tag => cardTagsUpper(card).includes(tag));
      }
      case ABILITY_CONDITION_TYPES.TARGET_META_VALUE_IN: {
        const key = filter.value?.key || filter.value?.meta_key;
        const expected = Array.isArray(filter.value?.values)
          ? filter.value.values
          : String(filter.value?.values || filter.value?.value || "").split(",");
        const normalized = expected.map(item => String(item).trim().toUpperCase()).filter(Boolean);
        const current = card?.meta?.[key];
        const values = Array.isArray(current) ? current : [current];
        return Boolean(key) && normalized.length > 0 && values
          .map(item => String(item).trim().toUpperCase())
          .some(item => normalized.includes(item));
      }
      case ABILITY_CONDITION_TYPES.TARGET_HAS_EQUIPMENT:
        return (card.equipments || []).length > 0;
      case ABILITY_CONDITION_TYPES.TARGET_IS_DAMAGED:
        return (parseInt(card.hp_remaining, 10) || 0) < (parseInt(card.hp, 10) || 0);
      case ABILITY_CONDITION_TYPES.TARGET_HAS_ENERGY:
        return (card.attached_energy || []).length > 0;
      case ABILITY_CONDITION_TYPES.TARGET_ENERGY_COUNT_GTE:
        return (card.attached_energy || []).length >= (parseInt(filter.value, 10) || 0);
      case ABILITY_CONDITION_TYPES.TARGET_HAS_CONDITION:
        return (card.status_effects || []).some(status => (
          !filter.value || filter.value === "ANY" || normalizeStatusFilterValue(filter.value) === status
        ));
      case ABILITY_CONDITION_TYPES.TARGET_IS_CARD_NAME:
        return card.name === filter.value;
      default:
        return false;
    }
  })();
  return filter.negate ? !result : result;
};

export const targetFiltersMatchCard = (card, filters = []) => (
  normalizeTargetFilters(filters).every(filter => targetFilterMatchesCard(card, filter))
);

